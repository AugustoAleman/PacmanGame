def boardSelector():
    key = randrange(0, 2, 1)

    global board

    if(key == 1):
        board = tiles
    else:
        board = tiles1


def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    global board

    if board[index] == 0:
        return False

    index = offset(point + 19)

    if board[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    global board

    for index in range(len(board)):
        tile = board[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    global board

    if board[index] == 1:
        board[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = []
            if (point.x - pacman.x) < 0:
                options.append(vector(5, 0)) # Pacman is more to the right ->
            elif (point.x - pacman.x) > 0:
                options.append(vector(-5, 0)) # Pacman is more to the left <-
            if (point.y - pacman.y) < 0:
                options.append(vector(0,-5)) # Pacman is downwards
            elif (point.y - pacman.y) > 0:
                options.append(vector(0, 5)) # Pacman is upwards
            plan = choice(options) # add the choices to the plan to plot the next course of the ghost
            course.x = plan.x
            course.y = plan.y
            if not valid(point + course):
                options = [vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]
                plan = choice(options) # add the choices to the plan to plot the next course of the ghost
                course.x = plan.x
                course.y = plan.y
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


